import { Arcana } from '../Classes/Persona';
export function ArcanaPair (arcana1: Arcana, arcana2: Arcana): number {
    if (arcana1 > arcana2) {
        return arcana2 * 21 + arcana1;
    } else {
        return arcana1 * 21 + arcana2;
    }
}

